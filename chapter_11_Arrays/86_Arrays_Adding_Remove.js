let arr = [1,2,3];
console.log(arr);

// add to END

arr.push(10);
console.log(arr);

// remove from End

arr.pop();
console.log(arr);

// push 2 number

arr.push(20,40);
console.log(arr);

//add to beginning

arr.unshift(100);
console.log(arr);

//remove to beginning
arr.shift(100);
console.log(arr);


